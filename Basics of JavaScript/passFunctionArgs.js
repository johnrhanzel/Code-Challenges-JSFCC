//passing values to functions with arguments

//parameters are variables that acts as placeholders for the values that are to be input to a function when called

function functionWithArgs(param1, param2){
    console.log(param1 + param2); //this will add the two parameters
  }
  
  functionWithArgs(1,2); //the output is 3
  functionWithArgs(7,9); //the output is 16